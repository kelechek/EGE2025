# Открываем текстовый файл и считываем первую строку
with open('24_14643.txt') as file:
    data = file.readline()

# Инициализация переменных
count = 3  # Начальное значение счетчика подряд идущих символов (минимум 3, чтобы избежать AXMM)
ans = 0  # Найденный максимальный результат

# Проходим по строке от начала до 4-го символа с конца
for i in range(len(data) - 3):
    letters = data[i:i + 4]  # Берем текущий срез из 4 символов
    if letters == 'AXMM':  # Если найдено AXMM
        count = 3  # Сбрасываем счетчик до 3
    else:
        count += 1  # Если не AXMM, увеличиваем счетчик
    ans = max(ans, count)  # Обновляем максимальное значение, если текущий счетчик больше

# Печатаем максимальное количество подряд идущих символов без AXMM
print(ans)
